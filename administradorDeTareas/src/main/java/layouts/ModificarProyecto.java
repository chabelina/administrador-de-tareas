  public void rellenarCampos(){
    List<String[]> datos = this.contAdmProy.obtenerDatosProyecto(this.id_user,this.id_proyecto);
    String nombre = datos.get(0)[0];
    String descrip = datos.get(0)[1];
    String cantPersonas = datos.get(0)[2];
    
    txt_cantidadProyecto.setText(cantPersonas);
    txt_descProyecto.setText(descrip);
    txt_nombreproyecto.setText(nombre);
    System.out.println("AUTOCOMPLETANDO LOS CAMPOS PARA MODIFICAR LOS DATOS DE UN PROYECTO");
  }

