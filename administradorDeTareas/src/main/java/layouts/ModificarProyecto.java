    private void btn_guardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarCambiosActionPerformed
      //EVENTO PARA MODIFICAR LA TAREA
      if( "".equals(txt_nombreproyecto.getText()) || "".equals(txt_descProyecto.getText()) || "".equals(txt_nombreproyecto.getText())){
          JOptionPane.showMessageDialog(null,"HAY CAMPOS VACIOS, VUELVA A INTENTAR");
      }else{
        int cantPer = Integer.parseInt(txt_cantidadProyecto.getText());
        String desc = txt_descProyecto.getText();
        String nombre = txt_nombreproyecto.getText();
        this.contAdmProy.guardarCambios(this.id_user, this.id_proyecto, nombre, desc, cantPer);
        System.out.println("SE MODIFICO LOS DATOS DEL PROYECTO");
      }
    }//GEN-LAST:event_btn_guardarCambiosActionPerformed

